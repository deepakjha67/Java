public class literals {
    public void main (String args[]) {
        
        // literals

        int num1 = 0b101;
        System.out.println(num1);     // Output : 5

        int num2 = 10_00_00_00_00;
        System.out.println(num2);     // Output: 1000000000

        double num3 = 12e10;
        System.out.println(num3);

        char c = 'a';
        System.out.println(c);       // Output: a


        c++;
        System.out.println(c);       // Output: b

   

    }   
}
